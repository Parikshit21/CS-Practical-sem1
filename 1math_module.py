#Q1 WAP to illustrate various function of math module.

import math as m
print(m.pi)                         #returns the value of pi
print(m.e)                          #returns the value of e
print(m.floor(11.63))               #returns greatest int less than input 
print(m.ceil(25.87))                #returns lowest int greater than input
print(m.factorial(5))               #returns factorial
print(m.lcm(13,42,4,10))            #returns lcm of multiple input
print(m.gcd(13,4,24,6))            #returns gcd of multiple input
print(m.comb(8,3))                 #returns no of ways of possible combination nCr
print(m.perm(8,3))                 #returns no of ways of possible permutation nPr
print(m.sqrt(38))                   #returns square root of input
print(m.log(64,2))                  #returns log64 with base 2
print(m.pow(2,5))                  #returns 10 to the power 5
print(m.cos(0))                     #returns cosine of input
print(m.sin(1))                     #returns sine of input
print(m.tan(2))                     #returns tan of input
print(m.asin(0))                    #returns inverse of sin of input
print(m.acos(0))                    #returns inverse of cos of input
print(m.atan(0))                    #returns inverse of tan of input
print(m.degrees(3.14))               #returns degree of radian input
print(m.radians(180))               #returns radian of degree input